// アクティビティバーの項目を識別するブランド型。
//
// なぜ string ではなく独自型か：
// - アクティビティバー内部のコードで「これはアクティビティの識別子だ」と
//   型が宣言されるため、他の文字列（ラベル、CSS クラス、タブID等）と
//   取り違えるとコンパイルエラーになる。
// - 実行時は単なる string なので、Map/Set のキーや属性値として
//   そのまま使え、性能ペナルティもない。
//
// 呼び出し側（外殻レイアウトを使うアプリ）は項目を作る境界で
// `アクティビティID("構築")` のように関数を通す。これが「ここが string→
// アクティビティID への昇格境界」だと読み取れる目印になる。

declare const アクティビティIDブランド: unique symbol;

export type アクティビティID = string & { readonly [アクティビティIDブランド]: void };

export function アクティビティID(値: string): アクティビティID {
    return 値 as アクティビティID;
}
