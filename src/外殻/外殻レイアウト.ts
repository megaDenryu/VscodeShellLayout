import { div, DivC, LV2部品集約Base } from "sengen-ui";
import type { HtmlComponentBase } from "sengen-ui";
import * as styles from './style.css';
import { 外殻レイアウト部品, type 外殻レイアウトオプション } from './外殻レイアウト部品';
import type { アクティビティID } from '../アクティビティバー/アクティビティID';
import type { Iレイアウトトグル操作 } from './レイアウトトグル操作';
import type { テーマ配色 } from '../テーマ/テーマ型';
import { テーマCSS変数名 } from '../テーマ/テーマCSS変数';

export type { 外殻レイアウトオプション };

// =============================================================================
// 使い方
// =============================================================================
//
// VSCodeライクなレイアウト（メニューバー / アクティビティバー / エディタエリア /
// パネル / 右サイドバー / ステータスバー）を一括で提供する LV2 Orchestrator。
//
// 基本的な使い方:
//
//   const シェル = new 外殻レイアウト({
//       タイトル: "アプリ名",
//       アクティビティ項目一覧: [
//           { id: アクティビティID("..."), ラベル: "...", アイコン: ... },
//           ...
//       ],
//       右サイドバー内容: サイドバーコンポーネント,
//       ステータスバー左: タイトルセレクタ,
//       ステータスバー右テキスト: "アプリ名",
//   });
//   document.body.appendChild(シェル.dom.element);
//
// スロットの注入は全てコンストラクタオプションで行う。
// `右サイドバーを取得する()` のような取得APIは廃止した（外部が late-mutate で
// スロットを書き換えるアンチパターンを防ぐため）。
//
// タブの追加・選択・イベント購読はタブAPIメソッドを通じて行う:
//   シェル.タブを追加する("id", "ラベル", コンポーネント)
//   シェル.タブを選択する("id")
//   シェル.onタブイベント({ onタブ選択: ..., onタブ閉じる: ... })
//
// アクティビティバーの選択通知:
//   シェル.onアクティビティ選択(id => { ... })  // id は アクティビティID
//
// 設定ボタン（アクティビティバー下部）の通知（非推奨。アクティビティバー下部項目一覧を使うこと）:
//   シェル.on設定クリック(() => { ... })
//
// 構成とデザインの自由化（2026-07-12）:
//   メニューバー表示 / ステータスバー表示: false で該当領域のDOM自体を作らない
//   メニューバートグルボタン表示: false で内蔵トグルを消し、サイドバートグルボタン /
//     パネルトグルボタン を任意のスロットに配線して置く
//   テーマ: Partial<テーマ配色> をルート要素にCSS変数として適用する
//
// Orchestrator の配線パターン（推奨）:
//
//   export class アプリシェル extends LV2部品集約Base<アプリシェル部品> {
//       protected _componentRoot: 外殻レイアウト;
//       constructor() {
//           super();
//           this._部品 = アプリシェル部品.作る();
//           this._componentRoot = this._ルートを構築する(this._部品);
//       }
//       protected _ルートを構築する(部品: アプリシェル部品): 外殻レイアウト {
//           return 部品.外殻;  // 部品.作る() の中で 外殻レイアウト({ ... 全スロットをここで渡す ... }) を構築する
//       }
//   }
//
// 外殻レイアウトは ID を全て string で扱う汎用シェル。
// 呼び出し側のドメイン固有ID（モードIDやタブ種別等）は、呼び出し側が
// 文字列との変換責任を持つ。シェル自身はドメインを知らない。

// =============================================================================
// Orchestrator（LV2部品集約: メニューバー/アクティビティバー/エディタエリア/パネル/
// サイドバー/ステータスバーというLV2部品群を集約・配置する）
// =============================================================================

export class 外殻レイアウト extends LV2部品集約Base<外殻レイアウト部品> implements Iレイアウトトグル操作 {
    protected _componentRoot: DivC;
    private readonly _部品: 外殻レイアウト部品;
    private _メインエリア: DivC | null = null;

    constructor(オプション: 外殻レイアウトオプション) {
        super();
        this._部品 = 外殻レイアウト部品.作る(オプション);
        this._componentRoot = this._ルートを構築する(this._部品);
        if (オプション.テーマ) {
            this._テーマを適用する(オプション.テーマ);
        }
    }

    protected _ルートを構築する(部品: 外殻レイアウト部品): DivC {
        // 領域のオプション化: メニューバー/ステータスバーは無効時、要素そのものを木に含めない
        // （data-attributeでの非表示ではなくDOM自体を作らない。ゼロ高さを保証する）。
        const メニューバー = 部品.メニューバー;
        const ステータスバー = 部品.ステータスバー;
        return (
            div({ class: styles.ルート }).childIfs([
                { If: メニューバー !== null, True: () => メニューバー ?? undefined },
                div({ class: styles.中央エリア }).childs([
                    部品.アクティビティバー,
                    div({ class: styles.メインエリア })
                        .tap(el => { this._メインエリア = el; })
                        .childs([
                            部品.エディタエリア,
                            部品.パネルスプリッター,
                            部品.パネルエリア]),
                    部品.サイドバースプリッター,
                    部品.右サイドバー]),
                { If: ステータスバー !== null, True: () => ステータスバー ?? undefined }])
        );
    }

    // テーマのCSSカスタムプロパティ化: ルート要素にトークンごとの --vsl-xxx を設定する。
    // CSS変数はDOMカスケードで子孫へ継承されるため、エディタエリアのタブに別アプリの
    // 外殻レイアウトを1タブとしてホストする「マトリョーシカシェル」構成（README参照）では、
    // 内側シェルのルートに別のテーマを渡すだけで内外の配色を描き分けられる
    // （内側のCSS変数が内側の subtree だけを上書きし、外側へは波及しない）。
    private _テーマを適用する(テーマ: Partial<テーマ配色>): void {
        let トークン: keyof テーマ配色;
        for (トークン in テーマCSS変数名) {
            const 値 = テーマ[トークン];
            if (値 !== undefined) {
                this._componentRoot.setCssVariable(テーマCSS変数名[トークン], 値);
            }
        }
    }

    // =========================================================================
    // タブ管理API
    // =========================================================================

    タブを追加する(id: string, ラベル: string, コンテンツ: HtmlComponentBase): void {
        this._部品.エディタエリア.タブを追加する(id, ラベル, コンテンツ);
    }

    基準タブを基準に分割追加する(
        基準タブid: string,
        方向: "左" | "右" | "上" | "下",
        id: string,
        ラベル: string,
        コンテンツ: HtmlComponentBase,
    ): void {
        this._部品.エディタエリア.基準タブを基準に分割追加する(基準タブid, 方向, id, ラベル, コンテンツ);
    }

    タブを選択する(id: string): void {
        this._部品.エディタエリア.タブを選択する(id);
    }

    タブが存在するか(id: string): boolean {
        return this._部品.エディタエリア.タブが存在するか(id);
    }

    タブ内ボタンを追加する(
        タブid: string,
        ボタンid: string,
        ラベル: string,
        onクリック: () => void,
    ): void {
        this._部品.エディタエリア.タブ内ボタンを追加する(タブid, ボタンid, ラベル, onクリック);
    }

    タブ再読み込みボタンを追加する(タブid: string, onクリック: () => void): void {
        this._部品.エディタエリア.タブ再読み込みボタンを追加する(タブid, onクリック);
    }

    onタブ選択(コールバック: (id: string) => void): void {
        this._部品.エディタエリア.イベントを設定する({
            onタブ選択: コールバック,
            onタブ閉じる: () => {},
        });
    }

    onタブイベント(イベント: { onタブ選択: (id: string) => void; onタブ閉じる: (id: string) => void }): void {
        this._部品.エディタエリア.イベントを設定する(イベント);
    }

    // =========================================================================
    // アクティビティバーAPI
    // =========================================================================

    onアクティビティ選択(コールバック: (id: アクティビティID) => void): void {
        this._部品.アクティビティバー.イベントを設定する({ on選択: コールバック });
    }

    アクティビティバッジ件数を設定する(id: アクティビティID, 件数: number): void {
        this._部品.アクティビティバー.バッジ件数を設定する(id, 件数);
    }

    /** @deprecated アクティビティバー下部項目一覧 で下部ボタンを注入し、onアクティビティ選択(id) で受け取ること */
    on設定クリック(コールバック: () => void): void {
        this._部品.アクティビティバー.on設定クリック(コールバック);
    }

    // =========================================================================
    // レイアウトトグル操作API（Iレイアウトトグル操作の実装）
    // =========================================================================
    //
    // メニューバー内蔵ボタンと同じ操作を、独立トグルボタン（サイドバートグルボタン/
    // パネルトグルボタン）やアプリ側の任意のUIから呼べるようにする。
    // シェルのインスタンスをそのまま `トグルボタン.配線する(シェル)` の形で渡せる。

    サイドバーを切り替える(): void {
        this._部品.トグル操作.サイドバーを切り替える();
    }

    パネルを切り替える(): void {
        this._部品.トグル操作.パネルを切り替える();
    }
}
