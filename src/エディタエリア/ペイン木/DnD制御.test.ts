import { describe, expect, it } from "vitest";
import { タブIDを作る, ペインIDを作る } from "./レイアウト型";
import {
    DnD適用,
    ヒットテスト,
    サブモード判定,
    タブバー離脱しきい値,
    ドラッグ開始しきい値,
    初期DnD状態,
    type DnD状態,
    type ペイン矩形情報,
    type 矩形,
} from "./DnD制御";

const タブA = タブIDを作る("a");
const タブB = タブIDを作る("b");
const ペイン1 = ペインIDを作る("p1");
const ペイン2 = ペインIDを作る("p2");
// ペイン1矩形 のタブ矩形一覧に含まれない外部タブ ID。ヒットテストの「ドラッグタブ除外」の影響を
// 受けずに既存の挙動を検証するため、別ペインからのドラッグを模擬する用途で使う。
const 外部タブ = タブIDを作る("external");

const ペイン1タブバー: 矩形 = { x: 0, y: 0, w: 100, h: 20 };

// 100x100 のペイン (0,0)。タブバーは y=0..20。
const ペイン1矩形: ペイン矩形情報 = {
    ペイン: ペイン1,
    矩形: { x: 0, y: 0, w: 100, h: 100 },
    タブ矩形一覧: [
        { タブ: タブA, 矩形: { x: 0, y: 0, w: 50, h: 20 } },
        { タブ: タブB, 矩形: { x: 50, y: 0, w: 50, h: 20 } },
    ],
    タブバー矩形: ペイン1タブバー,
    挿入可能: true,
};

function 押下中状態(座標 = { x: 0, y: 0 }): DnD状態 {
    return { kind: "押下中", ドラッグタブ: タブA, 開始座標: 座標, 元タブバー矩形: ペイン1タブバー };
}

function ペイン移動ドラッグ中(現在座標: { x: number; y: number }, 現在ターゲット: DnD状態 extends infer T ? T extends { kind: "ドラッグ中"; 現在ターゲット: infer X } ? X : never : never): DnD状態 {
    return {
        kind: "ドラッグ中",
        ドラッグタブ: タブA,
        開始座標: { x: 0, y: 0 },
        現在座標,
        元タブバー矩形: ペイン1タブバー,
        現在ターゲット,
        サブモード: "ペイン移動",
    };
}

describe("DnD適用 - 押下", () => {
    it("待機 → 押下中 へ遷移する", () => {
        const 結果 = DnD適用(初期DnD状態, {
            kind: "押下", タブ: タブA, 座標: { x: 10, y: 10 }, 元タブバー矩形: ペイン1タブバー,
        });
        expect(結果.新状態.kind).toBe("押下中");
        if (結果.新状態.kind === "押下中") {
            expect(結果.新状態.ドラッグタブ).toBe(タブA);
            expect(結果.新状態.開始座標).toEqual({ x: 10, y: 10 });
            expect(結果.新状態.元タブバー矩形).toEqual(ペイン1タブバー);
        }
        expect(結果.確定要求).toBeNull();
    });
});

describe("DnD適用 - 移動しきい値", () => {
    it("しきい値未満の移動は押下中のまま", () => {
        const 結果 = DnD適用(押下中状態(), {
            kind: "移動",
            座標: { x: ドラッグ開始しきい値 - 1, y: 0 },
            ペイン矩形一覧: [ペイン1矩形],
        });
        expect(結果.新状態.kind).toBe("押下中");
    });

    it("しきい値以上の移動でドラッグ中へ昇格", () => {
        const 結果 = DnD適用(押下中状態(), {
            kind: "移動",
            座標: { x: ドラッグ開始しきい値 + 1, y: 0 },
            ペイン矩形一覧: [ペイン1矩形],
        });
        expect(結果.新状態.kind).toBe("ドラッグ中");
        if (結果.新状態.kind === "ドラッグ中") {
            expect(結果.新状態.現在ターゲット).not.toBeNull();
        }
    });
});

describe("サブモード判定", () => {
    it("元タブバー null は常にペイン移動", () => {
        expect(サブモード判定({ x: 0, y: 0 }, null)).toBe("ペイン移動");
    });

    it("タブバー内は並べ替え", () => {
        expect(サブモード判定({ x: 50, y: 10 }, ペイン1タブバー)).toBe("並べ替え");
    });

    it("タブバー直下 (しきい値内) も並べ替え", () => {
        // タブバー高さ 20 の直下 30px 程度はしきい値 40 以内なので並べ替え扱い。
        expect(サブモード判定({ x: 50, y: 50 }, ペイン1タブバー)).toBe("並べ替え");
    });

    it("タブバーから縦しきい値以上離脱するとペイン移動", () => {
        const 下しきい値超え = ペイン1タブバー.y + ペイン1タブバー.h + タブバー離脱しきい値;
        expect(サブモード判定({ x: 50, y: 下しきい値超え + 1 }, ペイン1タブバー)).toBe("ペイン移動");
    });

    it("タブバー上方向にしきい値以上でもペイン移動", () => {
        const 上しきい値超え = ペイン1タブバー.y - タブバー離脱しきい値 - 1;
        expect(サブモード判定({ x: 50, y: 上しきい値超え }, ペイン1タブバー)).toBe("ペイン移動");
    });
});

describe("ヒットテスト - 並べ替えモード", () => {
    it("タブバー内 → タブ群挿入", () => {
        const 結果 = ヒットテスト({ x: 60, y: 10 }, [ペイン1矩形], "並べ替え", 外部タブ);
        expect(結果?.kind).toBe("タブ群挿入");
        if (結果?.kind === "タブ群挿入") {
            expect(結果.ペイン).toBe(ペイン1);
            // x=60 はタブ B 中央(75)より左 → 挿入位置 1
            expect(結果.挿入位置).toBe(1);
        }
    });

    it("タブバー外 (タブバー矩形外の同ペイン本体) は null (現状維持)", () => {
        const 結果 = ヒットテスト({ x: 50, y: 50 }, [ペイン1矩形], "並べ替え", 外部タブ);
        expect(結果).toBeNull();
    });

    it("別ペインのタブバー内 → そのペインへの挿入", () => {
        const ペイン2矩形: ペイン矩形情報 = {
            ペイン: ペイン2,
            矩形: { x: 100, y: 0, w: 100, h: 100 },
            タブ矩形一覧: [],
            タブバー矩形: { x: 100, y: 0, w: 100, h: 20 },
            挿入可能: true,
        };
        const 結果 = ヒットテスト({ x: 150, y: 10 }, [ペイン1矩形, ペイン2矩形], "並べ替え", 外部タブ);
        expect(結果?.kind).toBe("タブ群挿入");
        if (結果?.kind === "タブ群挿入") expect(結果.ペイン).toBe(ペイン2);
    });
});

describe("ヒットテスト - ペイン移動モード", () => {
    it("ペイン中央 (x=70, y=50) → タブ B 中央(75) より左、挿入位置 1", () => {
        const 結果 = ヒットテスト({ x: 70, y: 50 }, [ペイン1矩形], "ペイン移動", 外部タブ);
        expect(結果?.kind).toBe("タブ群挿入");
        if (結果?.kind === "タブ群挿入") {
            expect(結果.ペイン).toBe(ペイン1);
            // ペイン移動モードでタブバー外の中央は末尾挿入。
            expect(結果.挿入位置).toBe(2);
        }
    });

    it("左端 → 左分割 (タブバーなしペイン)", () => {
        const タブバーなし: ペイン矩形情報 = { ...ペイン1矩形, タブバー矩形: null };
        const 結果 = ヒットテスト({ x: 5, y: 50 }, [タブバーなし], "ペイン移動", 外部タブ);
        expect(結果?.kind).toBe("ペイン方向分割");
        if (結果?.kind === "ペイン方向分割") expect(結果.方向).toBe("左");
    });

    it("右端 → 右分割", () => {
        const タブバーなし: ペイン矩形情報 = { ...ペイン1矩形, タブバー矩形: null };
        const 結果 = ヒットテスト({ x: 95, y: 50 }, [タブバーなし], "ペイン移動", 外部タブ);
        expect(結果?.kind).toBe("ペイン方向分割");
        if (結果?.kind === "ペイン方向分割") expect(結果.方向).toBe("右");
    });

    it("上端 → 上分割 (タブバー矩形 null のペイン)", () => {
        const タブバーなし: ペイン矩形情報 = { ...ペイン1矩形, タブバー矩形: null };
        const 結果 = ヒットテスト({ x: 50, y: 5 }, [タブバーなし], "ペイン移動", 外部タブ);
        expect(結果?.kind).toBe("ペイン方向分割");
        if (結果?.kind === "ペイン方向分割") expect(結果.方向).toBe("上");
    });

    it("下端 → 下分割", () => {
        const タブバーなし: ペイン矩形情報 = { ...ペイン1矩形, タブバー矩形: null };
        const 結果 = ヒットテスト({ x: 50, y: 95 }, [タブバーなし], "ペイン移動", 外部タブ);
        expect(結果?.kind).toBe("ペイン方向分割");
        if (結果?.kind === "ペイン方向分割") expect(結果.方向).toBe("下");
    });

    it("ペイン外は null", () => {
        const 結果 = ヒットテスト({ x: 500, y: 500 }, [ペイン1矩形], "ペイン移動", 外部タブ);
        expect(結果).toBeNull();
    });

    it("挿入可能=false のペインは中央でも null", () => {
        const 挿入不可: ペイン矩形情報 = {
            ペイン: ペイン1, 矩形: { x: 0, y: 0, w: 100, h: 100 },
            タブ矩形一覧: [], タブバー矩形: null, 挿入可能: false,
        };
        const 結果 = ヒットテスト({ x: 50, y: 50 }, [挿入不可], "ペイン移動", 外部タブ);
        expect(結果).toBeNull();
    });

    it("挿入可能=false のペインでも端は方向分割が有効", () => {
        const 挿入不可: ペイン矩形情報 = {
            ペイン: ペイン1, 矩形: { x: 0, y: 0, w: 100, h: 100 },
            タブ矩形一覧: [], タブバー矩形: null, 挿入可能: false,
        };
        const 結果 = ヒットテスト({ x: 5, y: 50 }, [挿入不可], "ペイン移動", 外部タブ);
        expect(結果?.kind).toBe("ペイン方向分割");
    });
});

describe("DnD適用 - 離す (確定要求)", () => {
    it("押下中のまま離す → クリック相当、確定要求なし", () => {
        const 結果 = DnD適用(押下中状態(), {
            kind: "離す", 座標: { x: 1, y: 1 }, ペイン矩形一覧: [ペイン1矩形],
        });
        expect(結果.新状態.kind).toBe("待機");
        expect(結果.確定要求).toBeNull();
    });

    it("ドラッグ中ペイン移動 → 端で離すと方向分割が確定", () => {
        // 離す処理は サブモード判定 を再計算するため、座標を縦しきい値以上離す必要がある。
        // タブバー (y=0-20) + しきい値 40 = 60、y=65 でペイン移動モードに入る。
        const タブバーなし: ペイン矩形情報 = { ...ペイン1矩形, タブバー矩形: null };
        const ドラッグ中 = ペイン移動ドラッグ中(
            { x: 5, y: 65 },
            { kind: "ペイン方向分割", ペイン: ペイン1, 方向: "左" },
        );
        const 結果 = DnD適用(ドラッグ中, {
            kind: "離す", 座標: { x: 5, y: 65 }, ペイン矩形一覧: [タブバーなし],
        });
        expect(結果.確定要求?.kind).toBe("ペイン方向分割");
        if (結果.確定要求?.kind === "ペイン方向分割") {
            expect(結果.確定要求.方向).toBe("左");
        }
    });

    it("ドラッグ中 → ペイン外で離すと確定要求は出ず、タブは元位置に留まる", () => {
        const ドラッグ中 = ペイン移動ドラッグ中({ x: 500, y: 500 }, null);
        const 結果 = DnD適用(ドラッグ中, {
            kind: "離す", 座標: { x: 500, y: 500 }, ペイン矩形一覧: [ペイン1矩形],
        });
        expect(結果.新状態.kind).toBe("待機");
        expect(結果.確定要求).toBeNull();
    });

    it("離す座標と現在ターゲットが不整合でも、離す座標で再ヒットテストして判定する", () => {
        const ドラッグ中 = ペイン移動ドラッグ中(
            { x: 70, y: 50 },
            { kind: "タブ群挿入", ペイン: ペイン1, 挿入位置: 1 },
        );
        // 離す座標がペイン外なら最終ヒットテストで null → 確定要求なし
        const 結果 = DnD適用(ドラッグ中, {
            kind: "離す", 座標: { x: 500, y: 500 }, ペイン矩形一覧: [ペイン1矩形],
        });
        expect(結果.確定要求).toBeNull();
    });
});

describe("DnD適用 - 中断", () => {
    it("中断はどの状態からでも待機に戻り確定要求なし", () => {
        const ドラッグ中 = ペイン移動ドラッグ中(
            { x: 50, y: 50 },
            { kind: "タブ群挿入", ペイン: ペイン1, 挿入位置: 0 },
        );
        const 結果 = DnD適用(ドラッグ中, { kind: "中断" });
        expect(結果.新状態.kind).toBe("待機");
        expect(結果.確定要求).toBeNull();
    });
});

describe("DnD適用 - 完全シナリオ", () => {
    it("押下 → 移動(しきい値超え + 並べ替えモード) → 離す でタブ群挿入が確定", () => {
        let s: DnD状態 = 初期DnD状態;
        let r = DnD適用(s, {
            kind: "押下", タブ: タブA, 座標: { x: 0, y: 10 }, 元タブバー矩形: ペイン1タブバー,
        });
        s = r.新状態;
        expect(s.kind).toBe("押下中");

        r = DnD適用(s, {
            kind: "移動",
            座標: { x: 70, y: 10 }, // タブバー内
            ペイン矩形一覧: [ペイン1矩形],
        });
        s = r.新状態;
        expect(s.kind).toBe("ドラッグ中");
        if (s.kind === "ドラッグ中") expect(s.サブモード).toBe("並べ替え");

        r = DnD適用(s, { kind: "離す", 座標: { x: 70, y: 10 }, ペイン矩形一覧: [ペイン1矩形] });
        expect(r.新状態.kind).toBe("待機");
        expect(r.確定要求?.kind).toBe("タブ群挿入");
    });

    it("タブバーから縦しきい値以上引くとペイン移動モードへ遷移", () => {
        let s: DnD状態 = 初期DnD状態;
        let r = DnD適用(s, {
            kind: "押下", タブ: タブA, 座標: { x: 0, y: 10 }, 元タブバー矩形: ペイン1タブバー,
        });
        s = r.新状態;

        // タブバー内移動 → 並べ替え
        r = DnD適用(s, { kind: "移動", 座標: { x: 50, y: 10 }, ペイン矩形一覧: [ペイン1矩形] });
        s = r.新状態;
        if (s.kind === "ドラッグ中") expect(s.サブモード).toBe("並べ替え");

        // しきい値以上下に → ペイン移動
        const 下 = ペイン1タブバー.y + ペイン1タブバー.h + タブバー離脱しきい値 + 5;
        r = DnD適用(s, { kind: "移動", 座標: { x: 50, y: 下 }, ペイン矩形一覧: [ペイン1矩形] });
        s = r.新状態;
        if (s.kind === "ドラッグ中") expect(s.サブモード).toBe("ペイン移動");
    });
});

