import { describe, expect, it } from "vitest";
import {
    タブIDを作る,
    ペインIDを作る,
    空レイアウト,
    type タブ定義,
    type レイアウト,
    type タブ群ペイン,
    type 左右分割ペイン,
} from "./レイアウト型";
import { 適用, ペインを探す, 木の全タブ } from "./レイアウト操作";

const ルート = ペインIDを作る("ルート");

function 閉じれるタブ(id: string, ラベル: string): タブ定義 {
    return { id: タブIDを作る(id), ラベル, 閉じれる: true };
}

function 閉じれないタブ(id: string, ラベル: string): タブ定義 {
    return { id: タブIDを作る(id), ラベル, 閉じれる: false };
}

function 成功時のレイアウト(結果: ReturnType<typeof 適用>): レイアウト {
    if (結果.kind !== "成功") throw new Error(`適用失敗: ${JSON.stringify(結果)}`);
    return 結果.新レイアウト;
}

describe("レイアウト操作 - タブ追加", () => {
    it("空レイアウトに 1 件追加すると、そのタブが選択中になる", () => {
        const 初期 = 空レイアウト(ルート);
        const タブA = 閉じれるタブ("a", "A");
        const 結果 = 成功時のレイアウト(適用(初期, { kind: "タブ追加", タブ: タブA, 配置先ペイン: ルート }));
        const 群 = 結果.メインペイン as タブ群ペイン;
        expect(群.タブ一覧.length).toBe(1);
        expect(群.選択中).toBe(タブA.id);
    });

    it("複数追加しても最初の選択中は維持される", () => {
        let l = 空レイアウト(ルート);
        const タブA = 閉じれるタブ("a", "A");
        const タブB = 閉じれるタブ("b", "B");
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: タブA, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: タブB, 配置先ペイン: ルート }));
        const 群 = l.メインペイン as タブ群ペイン;
        expect(群.タブ一覧.map(t => t.id)).toEqual([タブA.id, タブB.id]);
        expect(群.選択中).toBe(タブA.id);
    });

    it("存在しないペインに追加すると失敗", () => {
        const 初期 = 空レイアウト(ルート);
        const 結果 = 適用(初期, { kind: "タブ追加", タブ: 閉じれるタブ("a", "A"), 配置先ペイン: ペインIDを作る("いない") });
        expect(結果.kind).toBe("失敗");
    });
});

describe("レイアウト操作 - タブ閉じる", () => {
    it("選択中タブを閉じると隣（直前）が選択される", () => {
        let l = 空レイアウト(ルート);
        const a = 閉じれるタブ("a", "A");
        const b = 閉じれるタブ("b", "B");
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: a, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: b, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ選択", タブ: b.id }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ閉じる", タブ: b.id }));
        const 群 = l.メインペイン as タブ群ペイン;
        expect(群.タブ一覧.length).toBe(1);
        expect(群.選択中).toBe(a.id);
    });

    it("閉じれないタブを閉じようとすると失敗", () => {
        let l = 空レイアウト(ルート);
        const 設定 = 閉じれないタブ("settings", "設定");
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: 設定, 配置先ペイン: ルート }));
        const 結果 = 適用(l, { kind: "タブ閉じる", タブ: 設定.id });
        expect(結果.kind).toBe("失敗");
        if (結果.kind === "失敗") expect(結果.理由.kind).toBe("閉じれないタブを閉じようとした");
    });

    it("最後のタブを閉じてもメインペインは保護される（空タブ群として残る）", () => {
        let l = 空レイアウト(ルート);
        const a = 閉じれるタブ("a", "A");
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: a, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ閉じる", タブ: a.id }));
        const 群 = l.メインペイン as タブ群ペイン;
        expect(群.kind).toBe("タブ群");
        expect(群.タブ一覧.length).toBe(0);
        expect(群.選択中).toBeNull();
    });
});

describe("レイアウト操作 - タブ→新ペイン分割", () => {
    it("右分割するとメインが左右分割になる", () => {
        let l = 空レイアウト(ルート);
        const a = 閉じれるタブ("a", "A");
        const b = 閉じれるタブ("b", "B");
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: a, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: b, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, {
            kind: "タブ→新ペイン分割",
            タブ: b.id,
            分割対象: ルート,
            方向: "右",
            新タブ群ペインID: ペインIDを作る("右"),
            新分割ペインID: ペインIDを作る("分割1"),
        }));
        const 分割 = l.メインペイン as 左右分割ペイン;
        expect(分割.kind).toBe("左右分割");
        expect(分割.id).toBe(ペインIDを作る("分割1"));
        expect((分割.左 as タブ群ペイン).id).toBe(ルート);
        expect((分割.左 as タブ群ペイン).タブ一覧.map(t => t.id)).toEqual([a.id]);
        expect((分割.右 as タブ群ペイン).id).toBe(ペインIDを作る("右"));
        expect((分割.右 as タブ群ペイン).タブ一覧.map(t => t.id)).toEqual([b.id]);
    });

    it("唯一のタブを左分割すると、空になった元ペインは縮約され分割が解消される", () => {
        let l = 空レイアウト(ルート);
        const a = 閉じれるタブ("a", "A");
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: a, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, {
            kind: "タブ→新ペイン分割",
            タブ: a.id,
            分割対象: ルート,
            方向: "左",
            新タブ群ペインID: ペインIDを作る("左"),
            新分割ペインID: ペインIDを作る("分割1"),
        }));
        const 全タブ = 木の全タブ(l.メインペイン);
        expect(全タブ.length).toBe(1);
        expect(全タブ[0].id).toBe(a.id);
    });

    it("分割後、元タブ群への後続タブ追加が正しく動く（Gemini G1+G2 回帰防止）", () => {
        let l = 空レイアウト(ルート);
        const a = 閉じれるタブ("a", "A");
        const b = 閉じれるタブ("b", "B");
        const c = 閉じれるタブ("c", "C");
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: a, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: b, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, {
            kind: "タブ→新ペイン分割",
            タブ: b.id,
            分割対象: ルート,
            方向: "右",
            新タブ群ペインID: ペインIDを作る("右"),
            新分割ペインID: ペインIDを作る("分割1"),
        }));
        // 元タブ群（id=ルート、左側）にタブ c を追加できることを確認。
        // 旧仕様（分割ペイン id = ルート 流用）だと深さ優先で親（分割ペイン）がヒットし、
        // タブ追加が「ペインが存在しない」(分割ペインはタブ群でない)で失敗する致命バグだった。
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: c, 配置先ペイン: ルート }));
        const 分割 = l.メインペイン as 左右分割ペイン;
        const 左群 = 分割.左 as タブ群ペイン;
        expect(左群.タブ一覧.map(t => t.id)).toEqual([a.id, c.id]);
    });
});


describe("レイアウト操作 - ペイン比率変更", () => {
    it("分割ペインの比率を変更できる", () => {
        let l = 空レイアウト(ルート);
        const a = 閉じれるタブ("a", "A");
        const b = 閉じれるタブ("b", "B");
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: a, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: b, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, {
            kind: "タブ→新ペイン分割",
            タブ: b.id,
            分割対象: ルート,
            方向: "右",
            新タブ群ペインID: ペインIDを作る("右"),
            新分割ペインID: ペインIDを作る("分割1"),
        }));
        // 比率変更は分割ペインID(= 新分割ペインID)を指定する。
        l = 成功時のレイアウト(適用(l, { kind: "ペイン比率変更", ペイン: ペインIDを作る("分割1"), 新比率: 0.7 }));
        const 分割 = l.メインペイン as 左右分割ペイン;
        expect(分割.比率).toBeCloseTo(0.7);
    });

    it("範囲外の比率は失敗", () => {
        const l = 空レイアウト(ルート);
        const 結果 = 適用(l, { kind: "ペイン比率変更", ペイン: ルート, 新比率: 1.5 });
        expect(結果.kind).toBe("失敗");
        if (結果.kind === "失敗") expect(結果.理由.kind).toBe("比率が範囲外");
    });
});

describe("レイアウト操作 - タブ移動", () => {
    it("同ペイン内で順序を入れ替えられる", () => {
        let l = 空レイアウト(ルート);
        const a = 閉じれるタブ("a", "A");
        const b = 閉じれるタブ("b", "B");
        const c = 閉じれるタブ("c", "C");
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: a, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: b, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: c, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ移動", タブ: c.id, 移動先ペイン: ルート, 挿入位置: 0 }));
        const 群 = l.メインペイン as タブ群ペイン;
        expect(群.タブ一覧.map(t => t.id)).toEqual([c.id, a.id, b.id]);
    });

    it("分割ペイン間でタブを移動できる", () => {
        let l = 空レイアウト(ルート);
        const a = 閉じれるタブ("a", "A");
        const b = 閉じれるタブ("b", "B");
        const c = 閉じれるタブ("c", "C");
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: a, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: b, 配置先ペイン: ルート }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ追加", タブ: c, 配置先ペイン: ルート }));
        const 右ID = ペインIDを作る("右");
        l = 成功時のレイアウト(適用(l, {
            kind: "タブ→新ペイン分割",
            タブ: c.id,
            分割対象: ルート,
            方向: "右",
            新タブ群ペインID: 右ID,
            新分割ペインID: ペインIDを作る("分割1"),
        }));
        l = 成功時のレイアウト(適用(l, { kind: "タブ移動", タブ: a.id, 移動先ペイン: 右ID, 挿入位置: 0 }));
        const 右ペイン = ペインを探す(l.メインペイン, 右ID) as タブ群ペイン;
        expect(右ペイン.タブ一覧.map(t => t.id)).toEqual([a.id, c.id]);
    });
});

describe("レイアウト操作 - 失敗ケース", () => {
    it("存在しないタブを選択 → 失敗", () => {
        const l = 空レイアウト(ルート);
        const 結果 = 適用(l, { kind: "タブ選択", タブ: タブIDを作る("いない") });
        expect(結果.kind).toBe("失敗");
        if (結果.kind === "失敗") expect(結果.理由.kind).toBe("タブが存在しない");
    });

    it("失敗時は元のレイアウトが変更されない（参照同一性）", () => {
        const l = 空レイアウト(ルート);
        適用(l, { kind: "タブ選択", タブ: タブIDを作る("いない") });
        // l 自体が不変オブジェクトなので、外側の参照を取れば同一であることが分かる
        expect((l.メインペイン as タブ群ペイン).タブ一覧.length).toBe(0);
    });
});
