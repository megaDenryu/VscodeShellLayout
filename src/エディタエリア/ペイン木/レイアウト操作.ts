function 末尾id<T extends { id: タブID }>(配列: readonly T[]): タブID | null {
    return 配列.length > 0 ? 配列[配列.length - 1].id : null;
}

function 失敗(理由: 失敗理由): 適用結果 {
    return { kind: "失敗", 理由 };
}

function 成功(新レイアウト: レイアウト): 適用結果 {
    return { kind: "成功", 新レイアウト };
}

function タブ追加(現在: レイアウト, タブ: タブ定義, 配置先ペイン: ペインID): 適用結果 {
    const 既存ペイン = ペインを探す(現在.メインペイン, 配置先ペイン);
    if (既存ペイン === null) {
        return 失敗({ kind: "ペインが存在しない", ペイン: 配置先ペイン });
    }
    if (既存ペイン.kind !== "タブ群") {
        return 失敗({ kind: "ペインが存在しない", ペイン: 配置先ペイン });
    }

    const 新メイン = ペインを置換(現在.メインペイン, 配置先ペイン, p => {
        if (p.kind !== "タブ群") return p;
        return {
            ...p,
            タブ一覧: [...p.タブ一覧, タブ],
            選択中: p.選択中 ?? タブ.id,
        };
    });
    return 成功({ メインペイン: 新メイン });
}

function タブ閉じる(現在: レイアウト, タブ: タブID): 適用結果 {
    const 所属 = タブを持つペインを探す(現在.メインペイン, タブ);
    if (所属 === null) return 失敗({ kind: "タブが存在しない", タブ });

    const 該当タブ定義 = 所属.タブ一覧.find(t => t.id === タブ)!;
    if (!該当タブ定義.閉じれる) {
        return 失敗({ kind: "閉じれないタブを閉じようとした", タブ });
    }

    const 新メイン = 全タブ群を変換(現在.メインペイン, p => {
        if (!p.タブ一覧.some(t => t.id === タブ)) return p;
        const 新タブ一覧 = p.タブ一覧.filter(t => t.id !== タブ);
        const 新選択中 = p.選択中 === タブ
            ? 末尾id(新タブ一覧)
            : p.選択中;
        return { ...p, タブ一覧: 新タブ一覧, 選択中: 新選択中 };
    });
    const 縮約後 = 空ペイン除去(新メイン, 現在.メインペイン.id);
    return 成功({ メインペイン: 縮約後 });
}

function タブ選択(現在: レイアウト, タブ: タブID): 適用結果 {
    const 所属 = タブを持つペインを探す(現在.メインペイン, タブ);
    if (所属 === null) return 失敗({ kind: "タブが存在しない", タブ });

    const 新メイン = 全タブ群を変換(現在.メインペイン, p =>
        p.タブ一覧.some(t => t.id === タブ) ? { ...p, 選択中: タブ } : p,
    );
    return 成功({ メインペイン: 新メイン });
}

function タブ移動(
    現在: レイアウト,
    タブ: タブID,
    移動先ペイン: ペインID,
    挿入位置: number,
): 適用結果 {
    const 所属 = タブを持つペインを探す(現在.メインペイン, タブ);
    if (所属 === null) return 失敗({ kind: "タブが存在しない", タブ });

    const 移動先 = ペインを探す(現在.メインペイン, 移動先ペイン);
    if (移動先 === null || 移動先.kind !== "タブ群") {
        return 失敗({ kind: "ペインが存在しない", ペイン: 移動先ペイン });
    }

    const 該当タブ定義 = 所属.タブ一覧.find(t => t.id === タブ)!;

    // 元位置から削除
    const 削除後 = 全タブ群を変換(現在.メインペイン, p =>
        p.タブ一覧.some(t => t.id === タブ)
            ? {
                ...p,
                タブ一覧: p.タブ一覧.filter(t => t.id !== タブ),
                選択中: p.選択中 === タブ
                    ? 末尾id(p.タブ一覧.filter(t => t.id !== タブ))
                    : p.選択中,
            }
            : p,
    );

    // 削除で空ペイン除去が走ると 移動先ペイン が消える可能性があるため、
    // 縮約は挿入後に行う。挿入時点で移動先が存在することを再確認。
    const 再取得 = ペインを探す(削除後, 移動先ペイン);
    if (再取得 === null || 再取得.kind !== "タブ群") {
        // 入口チェック後に分割ペインへ変わるケースは現状ないが、安全網として残す。
        return 失敗({ kind: "ペインが存在しない", ペイン: 移動先ペイン });
    }
    const 上限 = 再取得.タブ一覧.length;
    if (挿入位置 < 0 || 挿入位置 > 上限) {
        return 失敗({ kind: "挿入位置が範囲外", 値: 挿入位置, 上限 });
    }
    const 挿入後 = ペインを置換(削除後, 移動先ペイン, p => {
        if (p.kind !== "タブ群") return p;
        const 新タブ一覧 = [...p.タブ一覧];
        新タブ一覧.splice(挿入位置, 0, 該当タブ定義);
        return { ...p, タブ一覧: 新タブ一覧, 選択中: 該当タブ定義.id };
    });
    const 縮約 = 空ペイン除去(挿入後, 現在.メインペイン.id);
    return 成功({ メインペイン: 縮約 });
}

function タブから新ペイン分割(
    現在: レイアウト,
    タブ: タブID,
    分割対象: ペインID,
    方向: "左" | "右" | "上" | "下",
    新タブ群ペインID: ペインID,
    新分割ペインID: ペインID,
): 適用結果 {
    const 所属 = タブを持つペインを探す(現在.メインペイン, タブ);
    if (所属 === null) return 失敗({ kind: "タブが存在しない", タブ });

    const 対象 = ペインを探す(現在.メインペイン, 分割対象);
    if (対象 === null || 対象.kind !== "タブ群") {
        return 失敗({ kind: "ペインが存在しない", ペイン: 分割対象 });
    }

    const 該当タブ定義 = 所属.タブ一覧.find(t => t.id === タブ)!;

    const 削除後 = 全タブ群を変換(現在.メインペイン, p =>
        p.タブ一覧.some(t => t.id === タブ)
            ? {
                ...p,
                タブ一覧: p.タブ一覧.filter(t => t.id !== タブ),
                選択中: p.選択中 === タブ
                    ? 末尾id(p.タブ一覧.filter(t => t.id !== タブ))
                    : p.選択中,
            }
            : p,
    );

    const 再取得 = ペインを探す(削除後, 分割対象);
    if (再取得 === null) {
        return 失敗({ kind: "ペインが存在しない", ペイン: 分割対象 });
    }

    const 新タブ群: タブ群ペイン = {
        kind: "タブ群",
        id: 新タブ群ペインID,
        タブ一覧: [該当タブ定義],
        選択中: 該当タブ定義.id,
    };

    // 元タブ群の id はそのまま、新タブ群と分割ペインに別 ID を割り当てる
    // (同 ID 流用は親子探索で破綻する)。
    const 新メイン = ペインを置換(削除後, 分割対象, p => {
        switch (方向) {
            case "左":
                return { kind: "左右分割", id: 新分割ペインID, 左: 新タブ群, 右: p, 比率: 0.5 } as 左右分割ペイン;
            case "右":
                return { kind: "左右分割", id: 新分割ペインID, 左: p, 右: 新タブ群, 比率: 0.5 } as 左右分割ペイン;
            case "上":
                return { kind: "上下分割", id: 新分割ペインID, 上: 新タブ群, 下: p, 比率: 0.5 } as 上下分割ペイン;
            case "下":
                return { kind: "上下分割", id: 新分割ペインID, 上: p, 下: 新タブ群, 比率: 0.5 } as 上下分割ペイン;
        }
    });
    return 成功({ メインペイン: 空ペイン除去(新メイン, 現在.メインペイン.id) });
}

function ペイン比率変更(現在: レイアウト, ペイン: ペインID, 新比率: number): 適用結果 {
    if (新比率 < 0 || 新比率 > 1) {
        return 失敗({ kind: "比率が範囲外", 値: 新比率 });
    }
    const 対象 = ペインを探す(現在.メインペイン, ペイン);
    if (対象 === null || (対象.kind !== "左右分割" && 対象.kind !== "上下分割")) {
        return 失敗({ kind: "ペインが存在しない", ペイン });
    }
    const 新メイン = ペインを置換(現在.メインペイン, ペイン, p => {
        if (p.kind === "左右分割" || p.kind === "上下分割") {
            return { ...p, 比率: 新比率 };
        }
        return p;
    });
    return 成功({ メインペイン: 新メイン });
}
