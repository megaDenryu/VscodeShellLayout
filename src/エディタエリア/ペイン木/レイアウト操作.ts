// レイアウト操作: ペイン木に対する純関数群。
// 入力レイアウトを変更せず新レイアウトを返す。失敗時は失敗理由を明示し元のレイアウトは保持。
// 副作用なし、DOM 参照なし、テスト DOM 不要。

import type {
    タブID,
    タブ定義,
    ペイン,
    ペインID,
    タブ群ペイン,
    左右分割ペイン,
    上下分割ペイン,
    レイアウト,
    レイアウト操作,
    適用結果,
    失敗理由,
} from "./レイアウト型";

// =============================================================================
// 木探索ヘルパ
// =============================================================================

export function ペインを探す(木: ペイン, id: ペインID): ペイン | null {
    if (木.id === id) return 木;
    switch (木.kind) {
        case "タブ群":
            return null;
        case "左右分割":
            return ペインを探す(木.左, id) ?? ペインを探す(木.右, id);
        case "上下分割":
            return ペインを探す(木.上, id) ?? ペインを探す(木.下, id);
    }
}

export function タブを持つペインを探す(木: ペイン, タブ: タブID): タブ群ペイン | null {
    switch (木.kind) {
        case "タブ群":
            return 木.タブ一覧.some(t => t.id === タブ) ? 木 : null;
        case "左右分割":
            return タブを持つペインを探す(木.左, タブ) ?? タブを持つペインを探す(木.右, タブ);
        case "上下分割":
            return タブを持つペインを探す(木.上, タブ) ?? タブを持つペインを探す(木.下, タブ);
    }
}

export function 木の全ペイン(木: ペイン): ペイン[] {
    switch (木.kind) {
        case "タブ群":
            return [木];
        case "左右分割":
            return [木, ...木の全ペイン(木.左), ...木の全ペイン(木.右)];
        case "上下分割":
            return [木, ...木の全ペイン(木.上), ...木の全ペイン(木.下)];
    }
}

export function 木の全タブ(木: ペイン): タブ定義[] {
    return 木の全ペイン(木).flatMap(p => (p.kind === "タブ群" ? [...p.タブ一覧] : []));
}

// =============================================================================
// 木変換ヘルパ
// =============================================================================

export function ペインを置換(
    木: ペイン,
    対象ID: ペインID,
    置換関数: (ペイン: ペイン) => ペイン,
): ペイン {
    if (木.id === 対象ID) return 置換関数(木);
    switch (木.kind) {
        case "タブ群":
            return 木;
        case "左右分割": {
            const 新左 = ペインを置換(木.左, 対象ID, 置換関数);
            const 新右 = ペインを置換(木.右, 対象ID, 置換関数);
            if (新左 === 木.左 && 新右 === 木.右) return 木;
            return { ...木, 左: 新左, 右: 新右 };
        }
        case "上下分割": {
            const 新上 = ペインを置換(木.上, 対象ID, 置換関数);
            const 新下 = ペインを置換(木.下, 対象ID, 置換関数);
            if (新上 === 木.上 && 新下 === 木.下) return 木;
            return { ...木, 上: 新上, 下: 新下 };
        }
    }
}

export function 全タブ群を変換(
    木: ペイン,
    変換関数: (ペイン: タブ群ペイン) => タブ群ペイン,
): ペイン {
    switch (木.kind) {
        case "タブ群":
            return 変換関数(木);
        case "左右分割": {
            const 新左 = 全タブ群を変換(木.左, 変換関数);
            const 新右 = 全タブ群を変換(木.右, 変換関数);
            if (新左 === 木.左 && 新右 === 木.右) return 木;
            return { ...木, 左: 新左, 右: 新右 };
        }
        case "上下分割": {
            const 新上 = 全タブ群を変換(木.上, 変換関数);
            const 新下 = 全タブ群を変換(木.下, 変換関数);
            if (新上 === 木.上 && 新下 === 木.下) return 木;
            return { ...木, 上: 新上, 下: 新下 };
        }
    }
}

// メインペインだけは空タブ群でも残す (最初のペインを失わないため)。
export function 空ペイン除去(木: ペイン, 保護対象ID: ペインID): ペイン {
    switch (木.kind) {
        case "タブ群":
            return 木;
        case "左右分割": {
            const 新左 = 空ペイン除去(木.左, 保護対象ID);
            const 新右 = 空ペイン除去(木.右, 保護対象ID);
            if (新左.kind === "タブ群" && 新左.タブ一覧.length === 0 && 新左.id !== 保護対象ID) {
                return 新右;
            }
            if (新右.kind === "タブ群" && 新右.タブ一覧.length === 0 && 新右.id !== 保護対象ID) {
                return 新左;
            }
            if (新左 === 木.左 && 新右 === 木.右) return 木;
            return { ...木, 左: 新左, 右: 新右 };
        }
        case "上下分割": {
            const 新上 = 空ペイン除去(木.上, 保護対象ID);
            const 新下 = 空ペイン除去(木.下, 保護対象ID);
            if (新上.kind === "タブ群" && 新上.タブ一覧.length === 0 && 新上.id !== 保護対象ID) {
                return 新下;
            }
            if (新下.kind === "タブ群" && 新下.タブ一覧.length === 0 && 新下.id !== 保護対象ID) {
                return 新上;
            }
            if (新上 === 木.上 && 新下 === 木.下) return 木;
            return { ...木, 上: 新上, 下: 新下 };
        }
    }
}

// =============================================================================
// 純関数 適用()
// =============================================================================

export function 適用(現在: レイアウト, 操作: レイアウト操作): 適用結果 {
    switch (操作.kind) {
        case "タブ追加":
            return タブ追加(現在, 操作.タブ, 操作.配置先ペイン);
        case "タブ閉じる":
            return タブ閉じる(現在, 操作.タブ);
        case "タブ選択":
            return タブ選択(現在, 操作.タブ);
        case "タブ移動":
            return タブ移動(現在, 操作.タブ, 操作.移動先ペイン, 操作.挿入位置);
        case "タブ→新ペイン分割":
            return タブから新ペイン分割(
                現在, 操作.タブ, 操作.分割対象, 操作.方向,
                操作.新タブ群ペインID, 操作.新分割ペインID,
            );
        case "ペイン比率変更":
            return ペイン比率変更(現在, 操作.ペイン, 操作.新比率);
    }
}

