// pointer 系イベントから VSCode/Chrome 風のタブ DnD 状態を生成する純関数核。
// ドラッグ中のサブモード (並べ替え / ペイン移動) を座標に基づいて切り替える。
// 並べ替え中は元タブバーまたは別タブバーへの挿入位置を返し、配線層が毎フレーム
// レイアウト操作 タブ移動 を適用してリアルタイム並べ替えを実現する。
// 上下に縦しきい値以上引くとペイン移動モードに遷移し、端 25% 方向分割や別ペインへの
// 末尾挿入が判定される。
//
// 設計原則: 純粋ステートマシン (副作用なし)、HTML5 Drag-and-Drop でなく pointer 系。

import type { タブID, ペインID } from "./レイアウト型";

export interface 座標 {
    readonly x: number;
    readonly y: number;
}

export interface 矩形 {
    readonly x: number;
    readonly y: number;
    readonly w: number;
    readonly h: number;
}

export interface ペイン矩形情報 {
    readonly ペイン: ペインID;
    readonly 矩形: 矩形;
    // タブストリップ各タブの矩形 (挿入位置決定用)。分割ペインや空タブ群では空配列。
    readonly タブ矩形一覧: readonly { readonly タブ: タブID; readonly 矩形: 矩形 }[];
    // タブ群ペインのタブバー帯の矩形。分割ペインや非タブ群では null。
    // 並べ替えモードでは「タブバー上にあるか」、ペイン移動モードでは中央扱い検出に使う。
    readonly タブバー矩形: 矩形 | null;
    // 中央ゾーンへの末尾挿入を許可するか。タブ群ペインなら true、分割ペインなら false。
    readonly 挿入可能: boolean;
}

// =============================================================================
// ドロップターゲット
// =============================================================================

export type ドロップターゲット =
    | {
        // タブバー上の挿入位置 (並べ替え or 別タブバー挿入)。挿入位置はタブ矩形列から決定。
        readonly kind: "タブ群挿入";
        readonly ペイン: ペインID;
        readonly 挿入位置: number;
    }
    | {
        readonly kind: "ペイン方向分割";
        readonly ペイン: ペインID;
        readonly 方向: "左" | "右" | "上" | "下";
    };

// =============================================================================
// DnD 状態
// =============================================================================

export type DnD状態 =
    | { readonly kind: "待機" }
    | {
        readonly kind: "押下中";
        readonly ドラッグタブ: タブID;
        readonly 開始座標: 座標;
        // ドラッグタブが押下時に所属していたタブバー矩形。null は LV2 が計測できなかったケース。
        readonly 元タブバー矩形: 矩形 | null;
    }
    | {
        readonly kind: "ドラッグ中";
        readonly ドラッグタブ: タブID;
        readonly 開始座標: 座標;
        readonly 現在座標: 座標;
        readonly 元タブバー矩形: 矩形 | null;
        readonly 現在ターゲット: ドロップターゲット | null;
        // 並べ替え (= 元/別タブバー上) か、ペイン移動 (タブバーから縦しきい値以上離脱) か。
        readonly サブモード: "並べ替え" | "ペイン移動";
    };

export const 初期DnD状態: DnD状態 = { kind: "待機" };

// =============================================================================
// 確定要求 (離す時にレイアウト操作へ変換される)
// =============================================================================

export type 確定要求 =
    | {
        readonly kind: "タブ群挿入";
        readonly タブ: タブID;
        readonly 移動先ペイン: ペインID;
        readonly 挿入位置: number;
    }
    | {
        readonly kind: "ペイン方向分割";
        readonly タブ: タブID;
        readonly 分割対象: ペインID;
        readonly 方向: "左" | "右" | "上" | "下";
    };

// =============================================================================
// イベント
// =============================================================================

export type DnDイベント =
    | {
        readonly kind: "押下";
        readonly タブ: タブID;
        readonly 座標: 座標;
        // 押下時にドラッグタブが所属していたタブバー矩形 (LV2 が計測して渡す)。
        // null なら並べ替えモードの「元タブバー」基準が使えず、常にペイン移動モードになる。
        readonly 元タブバー矩形: 矩形 | null;
    }
    | { readonly kind: "移動"; readonly 座標: 座標; readonly ペイン矩形一覧: readonly ペイン矩形情報[] }
    | {
        // 離す座標で再ヒットテストし、移動中の中間状態に依存しない確定を行う。
        readonly kind: "離す";
        readonly 座標: 座標;
        readonly ペイン矩形一覧: readonly ペイン矩形情報[];
    }
    | { readonly kind: "中断" };

// =============================================================================
// しきい値
// =============================================================================

// 押下中 → ドラッグ中への昇格しきい値 (px)。これ未満の移動はクリック相当として扱う。
export const ドラッグ開始しきい値 = 5;

// 並べ替え → ペイン移動 への切り替え用、元タブバー矩形からの上下離脱しきい値 (px)。
// VSCode 風に「タブバーから一定距離以上離れたら掴んだタブが外れる」感覚を再現する。
// タブバー高さ (約 32px) より少し大きく取り、誤検知を防ぐ。
export const タブバー離脱しきい値 = 40;

// =============================================================================
// 純関数 DnD適用()
// =============================================================================

export interface DnD適用結果 {
    readonly 新状態: DnD状態;
    readonly 確定要求: 確定要求 | null;
}

export function DnD適用(現在: DnD状態, イベント: DnDイベント): DnD適用結果 {
    switch (イベント.kind) {
        case "押下":
            return 押下処理(イベント);
        case "移動":
            return 移動処理(現在, イベント);
        case "離す":
            return 離す処理(現在, イベント);
        case "中断":
            return { 新状態: 初期DnD状態, 確定要求: null };
    }
}

function 押下処理(イベント: { タブ: タブID; 座標: 座標; 元タブバー矩形: 矩形 | null }): DnD適用結果 {
    return {
        新状態: {
            kind: "押下中",
            ドラッグタブ: イベント.タブ,
            開始座標: イベント.座標,
            元タブバー矩形: イベント.元タブバー矩形,
        },
        確定要求: null,
    };
}

function 移動処理(
    現在: DnD状態,
    イベント: { 座標: 座標; ペイン矩形一覧: readonly ペイン矩形情報[] },
): DnD適用結果 {
    if (現在.kind === "待機") {
        return { 新状態: 現在, 確定要求: null };
    }
    if (現在.kind === "押下中") {
        const 距離 = 距離計算(現在.開始座標, イベント.座標);
        if (距離 < ドラッグ開始しきい値) {
            return { 新状態: 現在, 確定要求: null };
        }
        const サブモード = サブモード判定(イベント.座標, 現在.元タブバー矩形);
        return {
            新状態: {
                kind: "ドラッグ中",
                ドラッグタブ: 現在.ドラッグタブ,
                開始座標: 現在.開始座標,
                現在座標: イベント.座標,
                元タブバー矩形: 現在.元タブバー矩形,
                サブモード,
                現在ターゲット: ヒットテスト(イベント.座標, イベント.ペイン矩形一覧, サブモード, 現在.ドラッグタブ),
            },
            確定要求: null,
        };
    }
    const 新サブモード = サブモード判定(イベント.座標, 現在.元タブバー矩形);
    return {
        新状態: {
            ...現在,
            現在座標: イベント.座標,
            サブモード: 新サブモード,
            現在ターゲット: ヒットテスト(イベント.座標, イベント.ペイン矩形一覧, 新サブモード, 現在.ドラッグタブ),
        },
        確定要求: null,
    };
}

function 離す処理(
    現在: DnD状態,
    イベント: { 座標: 座標; ペイン矩形一覧: readonly ペイン矩形情報[] },
): DnD適用結果 {
    if (現在.kind === "待機") {
        return { 新状態: 現在, 確定要求: null };
    }
    if (現在.kind === "押下中") {
        return { 新状態: 初期DnD状態, 確定要求: null };
    }
    const サブモード = サブモード判定(イベント.座標, 現在.元タブバー矩形);
    const ターゲット = ヒットテスト(イベント.座標, イベント.ペイン矩形一覧, サブモード, 現在.ドラッグタブ);
    if (ターゲット === null) {
        return { 新状態: 初期DnD状態, 確定要求: null };
    }
    switch (ターゲット.kind) {
        case "タブ群挿入":
            return {
                新状態: 初期DnD状態,
                確定要求: {
                    kind: "タブ群挿入",
                    タブ: 現在.ドラッグタブ,
                    移動先ペイン: ターゲット.ペイン,
                    挿入位置: ターゲット.挿入位置,
                },
            };
        case "ペイン方向分割":
            return {
                新状態: 初期DnD状態,
                確定要求: {
                    kind: "ペイン方向分割",
                    タブ: 現在.ドラッグタブ,
                    分割対象: ターゲット.ペイン,
                    方向: ターゲット.方向,
                },
            };
    }
}

// =============================================================================
// サブモード判定
// =============================================================================

// 元タブバー矩形からの上下離脱距離で並べ替え / ペイン移動を切り替える。
// 元タブバーが null (計測できなかった場合) は常にペイン移動扱い。
export function サブモード判定(座標: 座標, 元タブバー矩形: 矩形 | null): "並べ替え" | "ペイン移動" {
    if (元タブバー矩形 === null) return "ペイン移動";
    const 上端 = 元タブバー矩形.y - タブバー離脱しきい値;
    const 下端 = 元タブバー矩形.y + 元タブバー矩形.h + タブバー離脱しきい値;
    if (座標.y < 上端 || 座標.y >= 下端) return "ペイン移動";
    return "並べ替え";
}

// =============================================================================
// ヒットテスト (サブモード別)
// =============================================================================

// ヒットテストの戻り値の挿入位置はすべて「ドラッグタブを取り除いたあとのタブ列に対する位置」に統一する。
// 視覚的にスライドして詰まったタブ列とユーザーの直感を一致させるため、ドラッグタブ自身は中央 x の比較から除外する。
export function ヒットテスト(
    座標: 座標,
    ペイン矩形一覧: readonly ペイン矩形情報[],
    サブモード: "並べ替え" | "ペイン移動",
    ドラッグタブ: タブID,
): ドロップターゲット | null {
    if (サブモード === "並べ替え") {
        return 並べ替えヒットテスト(座標, ペイン矩形一覧, ドラッグタブ);
    }
    return ペイン移動ヒットテスト(座標, ペイン矩形一覧, ドラッグタブ);
}

function 並べ替えヒットテスト(
    座標: 座標,
    ペイン矩形一覧: readonly ペイン矩形情報[],
    ドラッグタブ: タブID,
): ドロップターゲット | null {
    for (const 情報 of ペイン矩形一覧) {
        if (情報.タブバー矩形 === null) continue;
        if (!矩形に含まれる(座標, 情報.タブバー矩形)) continue;
        const 挿入位置 = ドラッグタブ除外挿入位置決定(座標, 情報.タブ矩形一覧, ドラッグタブ);
        return { kind: "タブ群挿入", ペイン: 情報.ペイン, 挿入位置 };
    }
    // タブバー外の並べ替えモード (上下離脱しきい値内の左右移動) は元タブバーが純核から特定できないため null。
    return null;
}

function ペイン移動ヒットテスト(
    座標: 座標,
    ペイン矩形一覧: readonly ペイン矩形情報[],
    ドラッグタブ: タブID,
): ドロップターゲット | null {
    for (const 情報 of ペイン矩形一覧) {
        if (!矩形に含まれる(座標, 情報.矩形)) continue;
        const タブバー上 = 情報.タブバー矩形 !== null && 矩形に含まれる(座標, 情報.タブバー矩形);
        if (!タブバー上) {
            const 方向 = 端ゾーン判定(座標, 情報.矩形);
            if (方向 !== null) {
                return { kind: "ペイン方向分割", ペイン: 情報.ペイン, 方向 };
            }
        }
        if (!情報.挿入可能) return null;
        // タブバー外の中央ドロップは末尾挿入。タブバー上ならそのタブバーの挿入位置を計算する。
        // 末尾挿入の長さも「ドラッグタブが居れば取り除いたあと」の長さに揃え、削除後ベースで一貫させる。
        const 挿入位置 = タブバー上
            ? ドラッグタブ除外挿入位置決定(座標, 情報.タブ矩形一覧, ドラッグタブ)
            : 削除後タブ数(情報.タブ矩形一覧, ドラッグタブ);
        return { kind: "タブ群挿入", ペイン: 情報.ペイン, 挿入位置 };
    }
    return null;
}

function 削除後タブ数(
    タブ矩形一覧: readonly { readonly タブ: タブID; readonly 矩形: 矩形 }[],
    ドラッグタブ: タブID,
): number {
    return タブ矩形一覧.some(t => t.タブ === ドラッグタブ)
        ? タブ矩形一覧.length - 1
        : タブ矩形一覧.length;
}

function 矩形に含まれる(p: 座標, r: 矩形): boolean {
    return p.x >= r.x && p.x < r.x + r.w && p.y >= r.y && p.y < r.y + r.h;
}

function 端ゾーン判定(p: 座標, r: 矩形): "左" | "右" | "上" | "下" | null {
    const 端帯比 = 0.25;
    const 左端境界 = r.x + r.w * 端帯比;
    const 右端境界 = r.x + r.w * (1 - 端帯比);
    const 上端境界 = r.y + r.h * 端帯比;
    const 下端境界 = r.y + r.h * (1 - 端帯比);

    const 左寄り = p.x < 左端境界;
    const 右寄り = p.x >= 右端境界;
    const 上寄り = p.y < 上端境界;
    const 下寄り = p.y >= 下端境界;

    if (!左寄り && !右寄り && !上寄り && !下寄り) {
        return null;
    }
    const dx = p.x - (r.x + r.w / 2);
    const dy = p.y - (r.y + r.h / 2);
    const 水平が強い = Math.abs(dx) * r.h > Math.abs(dy) * r.w;
    if (水平が強い) {
        return dx < 0 ? "左" : "右";
    }
    return dy < 0 ? "上" : "下";
}

// ドラッグタブを除いたタブ列の「視覚シフト後の中央 x」で挿入位置を決定する (削除後ベース)。
// 配線層は CSS transform で「ドラッグタブより右にいた他タブ」を左にドラッグタブ幅分シフトして
// 表示するため、判定もこの視覚位置に合わせないとユーザー直感とずれる
// (3 タブ以上の同ペイン並べ替えで「視覚で C を超えた」と思っても判定基準は元 C 中央のまま、
// という挙動を防ぐ)。
function ドラッグタブ除外挿入位置決定(
    p: 座標,
    タブ矩形一覧: readonly { readonly タブ: タブID; readonly 矩形: 矩形 }[],
    ドラッグタブ: タブID,
): number {
    const ドラッグindex = タブ矩形一覧.findIndex(t => t.タブ === ドラッグタブ);
    const ドラッグ幅 = ドラッグindex >= 0 ? タブ矩形一覧[ドラッグindex].矩形.w : 0;
    const 他タブ = タブ矩形一覧.filter(t => t.タブ !== ドラッグタブ);
    for (let i = 0; i < 他タブ.length; i++) {
        const 元index = タブ矩形一覧.findIndex(t => t.タブ === 他タブ[i].タブ);
        // ドラッグタブより右にいた他タブは視覚的にドラッグタブ幅分左にスライド済。
        // ドラッグタブが含まれていない (= 別ペインからのドラッグ) ならシフトなし。
        const 視覚シフト = ドラッグindex >= 0 && 元index > ドラッグindex ? -ドラッグ幅 : 0;
        const 中央x = 他タブ[i].矩形.x + 視覚シフト + 他タブ[i].矩形.w / 2;
        if (p.x < 中央x) return i;
    }
    return 他タブ.length;
}

function 距離計算(a: 座標, b: 座標): number {
    return Math.hypot(b.x - a.x, b.y - a.y);
}
